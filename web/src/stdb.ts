// SpacetimeDB client — the single connection that owns balances, transactions and
// notifications for the whole web service. Lives only here (Bun/TS-native); the Node
// bot reaches it over localhost HTTP.
//
// Design:
//  • One persistent connection, auto-reconnect.
//  • Global subscription to `account` → an always-fresh in-memory balance cache, so
//    HTTP balance reads are instant (no round-trip). Rows are tiny (id + i64).
//  • Per-user `notification` subscriptions, added on demand (when a user opens a WS
//    or fetches their inbox) and reference-counted so we don't hold every user's
//    inbox in memory.
//  • Reducer calls return a Promise that resolves on Committed / rejects on Failed,
//    matched FIFO per (reducer, key). Safe because a user has at most one in-flight
//    money op (client busy-flag + per-user rate limit).
//
// NOTE: `./module_bindings` is generated by:
//   spacetime generate --lang typescript --out-dir web/src/module_bindings --project-path spacetimedb
// Reducer/table names below follow STDB's codegen (snake_case reducer → camelCase method).

import { DbConnection, type ReducerEventContext } from "./module_bindings";

type Waiter = { resolve: () => void; reject: (e: Error) => void; timer: ReturnType<typeof setTimeout> };
type BalCb = (balance: number) => void;
type NotifRow = { id: bigint | number; owner: string; kind: string; amount: number | bigint; fromTag: string; msg: string; ts: number | bigint; read: boolean };
type NotifCb = (n: NotifRow) => void;

const REDUCER_TIMEOUT = 6000;

function num(v: number | bigint): number { return typeof v === "bigint" ? Number(v) : v; }

export class Stdb {
  private conn!: DbConnection;
  private identity?: any;
  private connected = false;
  private balances = new Map<string, number>();
  private notifs = new Map<string, NotifRow[]>();
  private notifSub = new Map<string, { handle: any; refs: number }>();
  private pending = new Map<string, Waiter[]>();               // "reducer:key" → FIFO queue
  private balCbs = new Map<string, Set<BalCb>>();
  private notifCbs = new Map<string, Set<NotifCb>>();
  private readyP?: Promise<void>;

  constructor(private uri: string, private moduleName: string, private token?: string) {}

  /** Connect (idempotent). Resolves once the account subscription is applied. */
  ready(): Promise<void> {
    if (!this.readyP) this.readyP = this.#connect();
    return this.readyP;
  }

  #connect(): Promise<void> {
    return new Promise((resolve, reject) => {
      let b = DbConnection.builder()
        .withUri(this.uri)
        .withDatabaseName(this.moduleName)
        .onConnect((conn, identity, _token) => {
          this.identity = identity;
          this.connected = true;
          this.#wireReducerResults(conn);
          // Global balance cache + live updates.
          conn.db.account.onInsert((_c, r: any) => this.#setBal(r.owner, num(r.balance)));
          conn.db.account.onUpdate((_c, _o: any, n: any) => this.#setBal(n.owner, num(n.balance)));
          conn.subscriptionBuilder()
            .onApplied(() => resolve())
            .subscribe(["SELECT * FROM account"]);
        })
        .onConnectError((_c, e) => reject(e instanceof Error ? e : new Error(String(e))))
        .onDisconnect(() => {
          this.connected = false;
          this.readyP = undefined;
          setTimeout(() => this.ready().catch(() => {}), 1500); // auto-reconnect
        });
      if (this.token) b = b.withToken(this.token);
      this.conn = b.build();
    });
  }

  #setBal(owner: string, balance: number) {
    this.balances.set(owner, balance);
    const cbs = this.balCbs.get(owner);
    if (cbs) for (const cb of cbs) try { cb(balance); } catch {}
  }

  // ── awaitable reducer calls ────────────────────────────────────────────────
  #wireReducerResults(conn: DbConnection) {
    const bind = (name: string, keyOf: (args: any[]) => string) => {
      const handler = (ctx: ReducerEventContext, ...args: any[]) => {
        // Only our own calls; match the oldest waiter for this (reducer,key).
        if (this.identity && ctx.event.callerIdentity && !ctx.event.callerIdentity.isEqual?.(this.identity)) {
          // Some SDK builds expose isEqual; if not, fall through (single-caller server).
        }
        const k = `${name}:${keyOf(args)}`;
        const q = this.pending.get(k);
        if (!q || !q.length) return;
        const w = q.shift()!;
        clearTimeout(w.timer);
        const st: any = ctx.event.status;
        if (st?.tag === "Committed") w.resolve();
        else w.reject(new Error(st?.value || st?.tag || "reducer failed"));
      };
      (conn.reducers as any)[`on${name[0].toUpperCase()}${name.slice(1)}`]?.(handler);
    };
    bind("ensureAccount", a => a[0]);
    bind("credit", a => a[0]);
    bind("deduct", a => a[0]);
    bind("settle", a => a[0]);
    bind("transfer", a => a[0]);          // key = `from`
    bind("setExact", a => a[0]);
    bind("addNotification", a => a[0]);
    bind("markRead", a => a[0]);
  }

  #call(name: string, key: string, fire: () => void): Promise<void> {
    return new Promise<void>((resolve, reject) => {
      const k = `${name}:${key}`;
      const timer = setTimeout(() => {
        const q = this.pending.get(k); if (q) { const i = q.findIndex(w => w.timer === timer); if (i >= 0) q.splice(i, 1); }
        reject(new Error("stdb timeout"));
      }, REDUCER_TIMEOUT);
      const q = this.pending.get(k) ?? []; q.push({ resolve, reject, timer }); this.pending.set(k, q);
      try { fire(); } catch (e) { clearTimeout(timer); reject(e as Error); }
    });
  }

  // ── public money API ─────────────────────────────────────────────────────
  async ensureAccount(owner: string) { await this.ready(); return this.#call("ensureAccount", owner, () => this.conn.reducers.ensureAccount(owner)); }
  async credit(owner: string, amount: number) { await this.ready(); return this.#call("credit", owner, () => this.conn.reducers.credit(owner, BigInt(amount))); }
  async deduct(owner: string, amount: number) { await this.ready(); return this.#call("deduct", owner, () => this.conn.reducers.deduct(owner, BigInt(amount))); }
  async settle(owner: string, bet: number, payout: number) { await this.ready(); return this.#call("settle", owner, () => this.conn.reducers.settle(owner, BigInt(bet), BigInt(payout))); }
  async transfer(from: string, to: string, amount: number, fromTag: string) { await this.ready(); return this.#call("transfer", from, () => this.conn.reducers.transfer(from, to, BigInt(amount), fromTag)); }
  async setExact(owner: string, balance: number) { await this.ready(); return this.#call("setExact", owner, () => this.conn.reducers.setExact(owner, BigInt(balance))); }
  async addNotification(owner: string, kind: string, amount: number, fromTag: string, msg: string) { await this.ready(); return this.#call("addNotification", owner, () => this.conn.reducers.addNotification(owner, kind, BigInt(amount), fromTag, msg)); }
  async markRead(owner: string) { await this.ready(); return this.#call("markRead", owner, () => this.conn.reducers.markRead(owner)); }

  /** Balance from the live cache (0 if unseen). */
  getBalance(owner: string): number { return this.balances.get(owner) ?? 0; }

  // ── balance push listeners (for WS) ────────────────────────────────────────
  onBalance(owner: string, cb: BalCb): () => void {
    const s = this.balCbs.get(owner) ?? new Set(); s.add(cb); this.balCbs.set(owner, s);
    return () => { s.delete(cb); if (!s.size) this.balCbs.delete(owner); };
  }

  // ── per-user notifications (on demand) ─────────────────────────────────────
  async subscribeNotifs(owner: string): Promise<void> {
    await this.ready();
    const ex = this.notifSub.get(owner);
    if (ex) { ex.refs++; return; }
    await new Promise<void>((resolve) => {
      const handle = this.conn.subscriptionBuilder()
        .onApplied(() => { this.#refreshNotifCache(owner); resolve(); })
        .subscribe([`SELECT * FROM notification WHERE owner = '${owner.replace(/'/g, "")}'`]);
      this.conn.db.notification.onInsert((_c, r: any) => { if (r.owner === owner) { this.#refreshNotifCache(owner); this.#emitNotif(owner, r); } });
      this.conn.db.notification.onUpdate((_c, _o: any, n: any) => { if (n.owner === owner) this.#refreshNotifCache(owner); });
      this.conn.db.notification.onDelete((_c, r: any) => { if (r.owner === owner) this.#refreshNotifCache(owner); });
      this.notifSub.set(owner, { handle, refs: 1 });
    });
  }
  unsubscribeNotifs(owner: string) {
    const ex = this.notifSub.get(owner); if (!ex) return;
    if (--ex.refs <= 0) { try { ex.handle?.unsubscribe?.(); } catch {} this.notifSub.delete(owner); this.notifs.delete(owner); }
  }
  #refreshNotifCache(owner: string) {
    const rows: NotifRow[] = [];
    for (const r of this.conn.db.notification.iter() as any) if (r.owner === owner) rows.push(r);
    rows.sort((a, b) => num(b.ts) - num(a.ts));
    this.notifs.set(owner, rows);
  }
  #emitNotif(owner: string, r: NotifRow) { const cbs = this.notifCbs.get(owner); if (cbs) for (const cb of cbs) try { cb(r); } catch {} }
  onNotification(owner: string, cb: NotifCb): () => void {
    const s = this.notifCbs.get(owner) ?? new Set(); s.add(cb); this.notifCbs.set(owner, s);
    return () => { s.delete(cb); if (!s.size) this.notifCbs.delete(owner); };
  }

  /** Read a user's notifications (subscribes if needed). Returns {items, unread}. */
  async getNotifications(owner: string): Promise<{ items: any[]; unread: number }> {
    await this.subscribeNotifs(owner);
    const rows = this.notifs.get(owner) ?? [];
    const items = rows.map(r => ({ t: r.kind, m: r.msg, a: num(r.amount), f: r.fromTag, ts: num(r.ts), read: r.read }));
    return { items, unread: items.filter(i => !i.read).length };
  }
}
